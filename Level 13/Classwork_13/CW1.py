# გააკეთეთ პატარა გულის ცემის სისტემა რომელიც ითვლის რა თქმა უნდა გულის ცემას  თუ 180 ზე მეტია ამ შემთხვევაში მაღალია თუ 160-დან 170 მდე არის ამ შემთვევაში არის ნორმალური თუ 160-ზე ნაკლებია ამ შემთხვევაში დაბალია

bpm = int(input("გთხოვთ შეიყვანოთ გულის ცემა: "))

if bpm >= 180:
    print("გულის ცემა მაღალია")
elif 160 <= bpm < 170:
    print("გულის ცემა ნორმალურია")
else :
    print("გულის ცემა დაბალია")