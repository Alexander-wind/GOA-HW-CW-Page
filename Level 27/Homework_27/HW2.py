lis1 = [1, 3, 5, 7, 9]
lis2 = [2, 4, 6, 8, 10]

def list_summerizer(x, y):
    mul1 = 0
    mul2 = 0
    for i in x:
        mul1 += i
    for i in y:
        mul2 += i 
    return mul1 * mul2

print(list_summerizer(lis1, lis2))